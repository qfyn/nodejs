/**
 * 预处理文本，处理换行和特殊字符
 */
function preprocessText(text) {
    let txt = text
        // 处理各种换行符
        .replace(/\r\n|\r|\n/g, '')
        // 替换中文标点为英文逗号，方便统一匹配
        .replace(/[，,。；;、]/g, ',')
        // 合并多个空格为一个
        .replace(/\s+/g, '')
        // 去除首尾空格
        .trim();
    txt = convertChineseNumbers(txt)
    return txt
}

/**
 * 将中文数字转换为阿拉伯数字
 * @param {string} text - 包含中文数字的文本
 * @returns {string} 转换后的文本
 */
function convertChineseNumbers(text) {
  const chineseNumbers = {
    '一': '1', '二': '2', '三': '3', '四': '4', '五': '5',
    '六': '6', '七': '7', '八': '8', '九': '9', '十': '10',
    '十一': '11', '十二': '12', '十三': '13', '十四': '14', '十五': '15',
    '两': '2', '零': '0'
  };
  
  // 单独处理"一名"、"二名"等情况
  const numberMap = {
    '一名': '1名', '二名': '2名', '三名': '3名', '四名': '4名', '五名': '5名',
    '六名': '6名', '七名': '7名', '八名': '8名', '九名': '9名', '十名': '10名',
    '一人': '1人', '二人': '2人', '三人': '3人', '四人': '4人', '五人': '5人',
    '六人': '6人', '七人': '7人', '八人': '8人', '九人': '9人', '十人': '10人'
  };
  
  let result = text;
  
  // 先替换"X名"、"X人"的情况
  for (const [chinese, arabic] of Object.entries(numberMap)) {
    result = result.replace(new RegExp(chinese, 'g'), arabic);
  }
  
  // 再替换单个中文数字
  for (const [chinese, arabic] of Object.entries(chineseNumbers)) {
    result = result.replace(new RegExp(chinese, 'g'), arabic);
  }
  
  return result;
}

/**
 * 批量提取函数，处理多个文本
 */
function extractMultipleSupervisorMeetingInfo(texts) {
    return texts.map(text => {
        const result = extractSupervisorMeetingInfo(text);
        return {
            text: text.length > 50 ? text.substring(0, 50) + '...' : text,
            result: result
        };
    });
}

