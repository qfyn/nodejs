/**
 * 从HTML元素中提取信息
 */
function extractFromElement(selector) {
    const element = document.querySelector(selector);
    if (!element) {
        console.error(`未找到元素: ${selector}`);
        return null;
    }
    
    const text = element.textContent || element.innerText;
    return extractSupervisorMeetingInfo(text);
}

